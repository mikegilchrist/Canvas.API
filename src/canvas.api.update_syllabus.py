# Purpose:      View, dump, or update a Canvas course's Syllabus page
#               (syllabus_body). Canvas's Syllabus page is stored in the
#               course object, not as a regular wiki page, so it needs a
#               dedicated endpoint.
#
# Usage:
#   canvas.api.update_syllabus.py COURSE_ID --get [-o OUT.html]
#   canvas.api.update_syllabus.py COURSE_ID --set BODY.html
#   canvas.api.update_syllabus.py COURSE_ID --replace OLD:NEW
#                                   [--replace OLD:NEW ...]
#
# Input:        Canvas API token + course_id
# Output:       Current syllabus_body to stdout (or file), or updated body
#
# Options:
#   --get             Print current syllabus_body to stdout
#   -o, --output PATH Write fetched body to PATH instead of stdout
#   --set PATH        Replace syllabus_body with contents of PATH (HTML)
#   --replace OLD:NEW Literal substring replacement in current body
#                     (repeatable). Useful for small in-place edits.
#   -n, --dry-run     Show what would change without writing
#   -v, --verbose     Detailed output
#   -h, --help        Show this help message
#
# Requirements:
#   - Canvas API token in ~/.canvas_token (or via --token/--token-file)
#   - ~/.canvas.api.conf with base_url and token_file
