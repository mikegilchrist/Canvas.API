#!/usr/bin/env python3
# ============================================================================
# Script:       canvas.api.fetch_gradebook.py
# Version:      1.0.0
# Date:         2026-04-08
# Purpose:      Fetch a Canvas course gradebook via the API and write a CSV
#               in the same column layout as the Canvas UI's gradebook
#               export. Useful when the per-course CSV export is not
#               available via the Reports API and you don't want to
#               download manually from the gradebook UI.
#
# Output CSV layout (matches Canvas gradebook export, minus the
# "Manual Posting" row which is generally not needed downstream):
#   Row 0: Headers ("Student", "ID", "SIS User ID", "SIS Login ID",
#                   "Section", then "<Assignment Name> (<assignment_id>)" ...)
#   Row 1: blank (placeholder for the Manual Posting row)
#   Row 2: "    Points Possible" + per-column points_possible values
#   Row 3+: one row per student
#
# Usage:        canvas.api.fetch_gradebook.py COURSE_ID
#                   [-o OUTPUT_PATH] [--groups GROUP1,GROUP2,...]
#                   [-n] [-v] [-h]
#
# Input:        Canvas API token + course_id
# Output:       Gradebook CSV at OUTPUT_PATH (default:
#               ./<TIMESTAMP>_Grades-COURSE.csv)
#
# Options:
#   -o, --output PATH     CSV output path (default: timestamped file in cwd)
#   --groups NAMES        Comma-separated assignment group names to include
#                         (default: all groups)
#   -n, --dry-run         Show what would be fetched without writing
#   -v, --verbose         Detailed progress output
#   -h, --help            Show this help message
#
# Requirements:
#   - Canvas API token in ~/.canvas_token (or via --token/--token-file)
#   - ~/.canvas.api.conf with base_url and token_file
#
# Session:      student.evaluation
