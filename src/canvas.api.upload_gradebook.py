# Version:      1.0.0
# Date:         2026-04-23
# Purpose:      Upload grades from a Canvas-format gradebook CSV to a Canvas
#               course. Assignment columns are matched by name or ID suffix.
#               Assignments not found in Canvas can be created automatically.
#
# Usage:        canvas.api.upload_gradebook.py COURSE_ID CSV_FILE
#                   [--create-missing] [--group-id ID] [--points N]
#                   [-n, --dry-run] [-v, --verbose] [-h, --help]
#
# Input:        Canvas-format gradebook CSV. Standard columns (Student, ID,
#               SIS User ID, SIS Login ID, Section) are used for student
#               lookup. Remaining columns are treated as assignments. Headers
#               may be plain names or "Name (assignment_id)" format.
#               Row with "Points Possible" in Student column is skipped.
#               Rows with empty Canvas user ID (column "ID") are skipped.
#
# Output:       Progress messages to stderr; summary to stdout.
#
# Options:
#   --create-missing    Create Canvas assignments not found by name/ID
#   --group-id ID       Assignment group ID for newly created assignments
#   --points N          Default points_possible for created assignments (1.0)
#   -n, --dry-run       Show actions without making changes
#   -v, --verbose       Show per-grade update messages
#   -h, --help          Show this help message
#
# Requirements:
#   - ~/.canvas.api.conf with base_url and token_file
#   - Canvas API token readable at token_file path
